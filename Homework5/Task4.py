"""
Написати функцію, що приймає безкінечну кількість чисел позиційними параметрами та іменований параметр start
зі значенням за замовчуванням 0. Функція має повертати суму усіх переданих параметрами  чисел та числа start.
Функція повинна називатися my_sum. Для розрахунку суми використайте вбудовану функцію sum.
"""


def my_sum(*sumik, start=0):
    sum_result = sum(sumik, start)
    return sum_result


result = my_sum(3, 5, 5)

print("Summ of nums: ", result)
